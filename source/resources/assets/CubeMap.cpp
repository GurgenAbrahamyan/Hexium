CubeMap::CubeMap() : VBO1( new VBO(cubeVertices, sizeof(cubeVertices), false)), VAO1(new VAO()) {
   

    VAO1->Bind();
    VAO1->LinkAttrib(*VBO1, 0, 3, GL_FLOAT, 3 * sizeof(float), (void*)0);
    VAO1->Unbind();
}

CubeMap::~CubeMap() {
    VBO1->~VBO();
    VAO1->~VAO();
}

void CubeMap::bind() const {
    VAO1->Bind();
}

void CubeMap::unbind() const {
	VAO1->Unbind();
}
